#[cfg(test)]
mod tests {
    use crate::*;

    #[test]
    fn test_mat2() {
        let v1 = Vec2 { x: 1.0, y: 0.0 };
        let v2 = Vec2 { x: -1.0, y: -1.0 };

        let mat = Mat2::scale(2.0) * Mat2::rot(std::f64::consts::FRAC_PI_2);

        let mv1 = mat * v1;
        let mv2 = mat * v2;

        assert!((mv1 - Vec2 { x: 0.0, y: 2.0 }).len() < 1.0e-10);
        assert!((mv2 - Vec2 { x: 2.0, y: -2.0 }).len() < 1.0e-10);
    }
}
