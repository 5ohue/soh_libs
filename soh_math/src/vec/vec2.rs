//-----------------------------------------------------------------------------
use macros::impl_vec;
use num_traits::Float;
//-----------------------------------------------------------------------------
#[repr(C)]
#[impl_vec]
pub struct Vec2<F> {
    pub x: F,
    pub y: F,
}

//-----------------------------------------------------------------------------
// Math functions
impl<F> Vec2<F>
where
    F: Float,
{
    pub fn cross(vec1: &Self, vec2: &Self) -> F {
        return vec1.x * vec2.y - vec1.y * vec2.x;
    }
}

